    <script>
      window.addEventListener('load', function()
      {
        let currentSlide = 0;
        const slides = document.querySelectorAll('#slider-container > div');
        const dots = document.querySelectorAll('.slider-dot');
        const container = document.getElementById('slider-container');

        function updateSlider()
        {
          container.style.transform = `translateX(-${currentSlide * 100}%)`;
          dots.forEach((dot, index) =>
          {
            if (index === currentSlide)
            {
              dot.classList.remove('bg-gray-300');
              dot.classList.add('bg-primary');
            }
            else
            {
              dot.classList.remove('bg-primary');
              dot.classList.add('bg-gray-300');
            }
          });
        }

        function nextSlide()
        {
          currentSlide = (currentSlide + 1) % slides.length;
          updateSlider();
        }

        setInterval(nextSlide, 4000);

        let touchStartX = 0;
        let touchEndX = 0;

        container.parentElement.addEventListener('touchstart', function(e)
        {
          touchStartX = e.changedTouches[0].screenX;
        }, false);

        container.parentElement.addEventListener('touchend', function(e)
        {
          touchEndX = e.changedTouches[0].screenX;
          if (touchStartX - touchEndX > 50)
          {
            currentSlide = (currentSlide + 1) % slides.length;
            updateSlider();
          }
          if (touchEndX - touchStartX > 50)
          {
            currentSlide = (currentSlide - 1 + slides.length) % slides
              .length;
            updateSlider();
          }
        }, false);
      });
    </script>
